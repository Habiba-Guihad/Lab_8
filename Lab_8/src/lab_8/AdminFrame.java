    private void fillDetails() {
        int row = pendingTable.getSelectedRow();
        if (row == -1) return;

        String id = pendingTable.getValueAt(row, 0).toString();
        String title = pendingTable.getValueAt(row, 1).toString();
        String instructor = pendingTable.getValueAt(row, 2).toString();
        String desc = "Course description here"; // replace with backend fetch

        courseIdLabel.setText("Course ID: " + id);
        titleLabel.setText("Title: " + title);
        instructorLabel.setText("Instructor: " + instructor);
        descLabel.setText("<html>Description:<br>" + desc + "</html>");
    }

