// Load only courses created by this instructor
private void loadCourses() {
tableModel.setRowCount(0); // clear existing rows

List<Course> myCourses = courseManager.getCoursesByInstructor(instructor.getUserId());

for (Course c : myCourses) {
    tableModel.addRow(new Object[]{
        c.getCourseId(),
        c.getTitle(),
        c.getDescription()
    });
}

addExtraRows(5);


}

// Add empty rows for spacing/input
private void addExtraRows(int extraRows) {
for (int i = 0; i < extraRows; i++) {
tableModel.addRow(new Object[]{"", "", ""});
}
}

// Refresh courses table (use after add/edit/delete)
public void refreshCourses() {
tableModel.setRowCount(0);
loadCourses();
}



