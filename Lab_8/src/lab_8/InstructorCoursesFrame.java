    private void CreateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateButtonActionPerformed

int rowCount = tableModel.getRowCount();
boolean added = false;

for (int row = 0; row < rowCount; row++) {
    String courseIdCell = (tableModel.getValueAt(row, 0) != null) ? tableModel.getValueAt(row, 0).toString() : "";
    String titleCell = (tableModel.getValueAt(row, 1) != null) ? tableModel.getValueAt(row, 1).toString().trim() : "";

    // Only create if the row has a title but no ID yet
    if (courseIdCell.isEmpty() && !titleCell.isEmpty()) {
        String description = (tableModel.getValueAt(row, 2) != null) ? tableModel.getValueAt(row, 2).toString() : "";

        // Create course via CourseManager
        Course newCourse = courseManager.addCourse(titleCell, description, instructor.getUserId());

        if (newCourse != null) {
            // Update instructor's created courses
            if (!instructor.getCreatedCourses().contains(newCourse.getCourseId())) {
                  instructor.addCourse(courseManager, titleCell, description) ;

            }

            // Update table with generated course ID
            tableModel.setValueAt(newCourse.getCourseId(), row, 0);

            // Save instructor changes to JSON

            dbManager.saveCourses();
            dbManager.saveUsers();

            added = true;
        }

        break; // only create one course per click
    }
}

if (added) {
    refreshCourses(); // reload table to show new course
    JOptionPane.showMessageDialog(this, "Course created successfully!");
} else {
    JOptionPane.showMessageDialog(this, "Please enter a course title to create a course.");
}

    }//GEN-LAST:event_CreateButtonActionPerformed

