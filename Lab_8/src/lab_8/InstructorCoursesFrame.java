    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed

int rowCount = tableModel.getRowCount();
boolean edited = false;
for (int row = 0; row < rowCount; row++) {
    String courseIdCell = (tableModel.getValueAt(row, 0) != null) ? tableModel.getValueAt(row, 0).toString() : "";
    String titleCell = (tableModel.getValueAt(row, 1) != null) ? tableModel.getValueAt(row, 1).toString().trim() : "";
    String descriptionCell = (tableModel.getValueAt(row, 2) != null) ? tableModel.getValueAt(row, 2).toString() : "";

    if (!courseIdCell.isEmpty()) {
        Course c = courseManager.getCourse(courseIdCell);
        if (c != null && c.getInstructorId().equals(instructor.getUserId())) {
            if (!c.getTitle().equals(titleCell) || !c.getDescription().equals(descriptionCell)) {
                c.setTitle(titleCell);
                c.setDescription(descriptionCell);
                edited = true;
            }
        }
    }
}

if (edited) {
    dbManager.saveCourses();
    JOptionPane.showMessageDialog(this, "Courses updated successfully!");
    refreshCourses();
} else {
    JOptionPane.showMessageDialog(this, "No changes detected.");
}
    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
      int selectedRow = CoursesTable.getSelectedRow();
if (selectedRow == -1) {
JOptionPane.showMessageDialog(this, "Please select a course to delete.");
return;
}
String courseId = (tableModel.getValueAt(selectedRow, 0) != null) ? tableModel.getValueAt(selectedRow, 0).toString() : "";
if (courseId.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Cannot delete an empty row.");
    return;
}

Course c = courseManager.getCourse(courseId);
if (c != null && c.getInstructorId().equals(instructor.getUserId())) {
    courseManager.deleteCourse(courseId); // removes from courses list and saves courses.json
    instructor.getCreatedCourses().remove(courseId);
    dbManager.saveUsers();       // save updated instructor list
    tableModel.removeRow(selectedRow);
    JOptionPane.showMessageDialog(this, "Course deleted successfully!");
} else {
    JOptionPane.showMessageDialog(this, "You do not have permission to delete this course.");
}
    }//GEN-LAST:event_DeleteButtonActionPerformed

