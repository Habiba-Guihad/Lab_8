public InstructorCreateQuiz(Instructor instructor, CourseManager courseManager,LessonManager lessonManager) {
    this.instructor = instructor;
    this.courseManager =courseManager;
    this.lessonManager = lessonManager;

    initComponents();  // NetBeans generated UI setup

    // Load courses immediately
    loadCourses();
    lessonComboBox.setEnabled(false);
    createQuizButton.setEnabled(false);

    // Automatically populate lessons when the first course is selected
    if (courseComboBox.getItemCount() > 0) {
        courseComboBox.setSelectedIndex(0);
        loadSelectedCourseLessons();
    }

    // Add create button action
    createQuizButton.addActionListener(e -> createQuizManually());
}

