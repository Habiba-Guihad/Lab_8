// -------------------- Helper Methods --------------------

private void loadCourses() {
    courseComboBox.removeAllItems();
    for (Course c : courseManager.getCoursesByInstructor(instructor.getUserId())) {
        courseComboBox.addItem(c.getCourseId() + " - " + c.getTitle());
    }
}

private void loadSelectedCourseLessons() {
    lessonComboBox.removeAllItems();
    String courseId = getSelectedCourseId();
    if (courseId == null) return;

    Course course = courseManager.getCourse(courseId);
    if (course != null) {
        for (Lesson lesson : lessonManager.getAllLessons(course)) {
            lessonComboBox.addItem(lesson.getLessonId() + " - " + lesson.getTitle());
        }
        lessonComboBox.setEnabled(true);
    }
}

private String getSelectedCourseId() {
    String selected = (String) courseComboBox.getSelectedItem();
    if (selected == null) return null;
    return selected.split(" - ")[0];
}

private String getSelectedLessonId() {
    String selected = (String) lessonComboBox.getSelectedItem();
    if (selected == null) return null;
    return selected.split(" - ")[0];
}

private void createQuizManually() {
    String courseId = getSelectedCourseId();
    String lessonId = getSelectedLessonId();
    String quizTitle = quizTitleField.getText().trim();

    if (courseId == null || lessonId == null || quizTitle.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Select course, lesson, and enter quiz title.");
        return;
    }

    boolean created = instructor.addQuiz(lessonManager, courseManager,courseId, lessonId, quizTitle, 0, new java.util.ArrayList<>());

    if (created) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Quiz created! Now you can add questions.");
        // TODO: Open questions frame for adding questions
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Failed to create quiz. Check your selections.");
    }
}



